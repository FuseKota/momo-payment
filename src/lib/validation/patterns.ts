/**
 * 入力検証の正規表現（クライアントのフォーム検証とサーバーの Zod スキーマで共用する単一ソース）。
 */

/** メールアドレス（簡易） */
export const EMAIL_REGEX = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;

/** 日本の電話番号: 090-1234-5678 / 09012345678 / 03-1234-5678 等 */
export const PHONE_REGEX = /^0[0-9\-]{9,13}$/;

/** 日本の郵便番号: 123-4567 または 1234567 */
export const POSTAL_CODE_REGEX = /^\d{3}-?\d{4}$/;
